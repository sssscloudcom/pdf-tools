const fs = require('fs')
const path = require('path')

const supportedLanguages = ['en', 'zh', 'es', 'de', 'ja', 'fr', 'ru', 'pt', 'id', 'ar']
const routes = [
  { path: '/', titleKey: 'site.title', descKey: 'site.description' },
  { path: '/compress-pdf', titleKey: 'tools.compressPdf.title', descKey: 'tools.compressPdf.description' },
  { path: '/compress-image', titleKey: 'tools.compressImage.title', descKey: 'tools.compressImage.description' },
  { path: '/pdf-to-jpg', titleKey: 'tools.pdfToJpg.title', descKey: 'tools.pdfToJpg.description' },
  { path: '/jpg-to-pdf', titleKey: 'tools.jpgToPdf.title', descKey: 'tools.jpgToPdf.description' },
]

// Helper: get nested value from object
function getNestedValue(obj, key) {
  return key.split('.').reduce((o, k) => o && o[k], obj)
}

// Read base index.html
const baseHtml = fs.readFileSync('dist/index.html', 'utf-8')

// Load translations
const translations = {}
supportedLanguages.forEach(lang => {
  try {
    const langFile = fs.readFileSync(`dist/locales/${lang}/translation.json`, 'utf-8')
    translations[lang] = JSON.parse(langFile)
  } catch (e) {
    try {
      const langFile = fs.readFileSync(`public/locales/${lang}.json`, 'utf-8')
      translations[lang] = JSON.parse(langFile)
    } catch (e2) {
      console.error(`❌ Cannot load translations for ${lang}`)
      translations[lang] = {}
    }
  }
})

// Generate hreflang tags
function generateHreflangTags(currentPath) {
  let tags = ''
  supportedLanguages.forEach(lang => {
    const url = `https://pdftools.nextapi.pro/${lang}${currentPath}`
    tags += `    <link rel="alternate" hreflang="${lang}" href="${url}" />\n`
  })
  tags += `    <link rel="alternate" hreflang="x-default" href="https://pdftools.nextapi.pro/en${currentPath}" />\n`
  return tags
}

// Collect redirect rules
const redirectRules = []

// Generate HTML for each language and route
console.log('Generating multilingual HTML files...')
supportedLanguages.forEach(lang => {
  routes.forEach(route => {
    const outputPath = route.path === '/' 
      ? `dist/${lang}/index.html` 
      : `dist/${lang}${route.path}/index.html`
    const outputDir = path.dirname(outputPath)
    
    // Create directory
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true })
    }
    
    // Get translations
    const t = translations[lang]
    const title = getNestedValue(t, route.titleKey) || 'PDF Tools'
    const desc = getNestedValue(t, route.descKey) || 'Free online PDF tools'
    
    // Generate hreflang tags
    const hreflangTags = generateHreflangTags(route.path)
    
    // Replace HTML
    let html = baseHtml
      .replace(/<html lang="en">/, `<html lang="${lang}">`)
      .replace(/<title>.*<\/title>/, `<title>${title}</title>`)
      .replace(/<meta name="description" content=".*" \/>/, `<meta name="description" content="${desc}" />`)
      .replace(/<meta property="og:title" content=".*" \/>/, `<meta property="og:title" content="${title}" />`)
      .replace(/<meta property="og:description" content=".*" \/>/, `<meta property="og:description" content="${desc}" />`)
      .replace(/<meta name="twitter:title" content=".*" \/>/, `<meta name="twitter:title" content="${title}" />`)
      .replace(/<meta name="twitter:description" content=".*" \/>/, `<meta name="twitter:description" content="${desc}" />`)
      .replace(/<!-- Hreflang will be dynamically generated by react-helmet -->/, hreflangTags)
      .replace(/<!-- Canonical will be dynamically updated -->/, `    <link rel="canonical" href="https://pdftools.nextapi.pro/${lang}${route.path}" />`)
    
    // Write file
    fs.writeFileSync(outputPath, html)
    console.log(`✅ Generated: ${outputPath}`)
    
    // Add redirect rule for non-root routes (add trailing slash)
    if (route.path !== '/') {
      redirectRules.push(`/${lang}${route.path} /${lang}${route.path}/ 301`)
    }
  })
})

// Generate _redirects file
const redirectsContent = `# Auto-generated by generate-multilingual.cjs\n# Trailing slash redirects for Cloudflare Pages\n${redirectRules.join('\n')}\n`
fs.writeFileSync('dist/_redirects', redirectsContent)
console.log(`\n✅ Generated _redirects with ${redirectRules.length} rules`)

console.log(`\n✅ All multilingual HTML files generated successfully!`)
console.log(`Total: ${supportedLanguages.length * routes.length} HTML files`)